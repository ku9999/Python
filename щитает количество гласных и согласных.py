a=input()
s=['а', 'у', 'о', 'ы', 'и', 'э', 'я', 'ю', 'ё', 'е']
p=['б', 'в', 'г', 'д', 'ж', 'з', 'й', 'к', 'л', 'м', 'н','п', 'р', 'с', 'т', 'ф', 'х', 'ц', 'ч', 'ш', 'щ']
k=0
j=0
for i in a.lower():
    if i in s:
        k+=1
    elif i in p.lower():
        j+=1
print('Количество гласных букв равно',k)
print('Количество согласных букв равно',j)
